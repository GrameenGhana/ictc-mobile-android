package com.grameenfoundation.ictc.wrapper;

/**
 *
 * @author skwakwa
 */
public class MeetingActivityWrapper {
    
    private int index;
    private String name;

    public MeetingActivityWrapper() {
    }

    
    
    public MeetingActivityWrapper(int index, String name) {
        this.index = index;
        this.name = name;
    }

    /**
     * @return the index
     */
    public int getIndex() {
        return index;
    }

    /**
     * @param index the index to set
     */
    public void setIndex(int index) {
        this.index = index;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
    
    
    
}
